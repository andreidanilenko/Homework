#Для форматирования времени изменения файла из результата os.stat()
# в человеко-читаемый формат можно использовать следующий подход:

import os
import time
from datetime import datetime

file_path = 'путь_к_вашему_файлу'
file_stat = os.stat(file_path)

# Получаем время изменения файла
modification_time = file_stat.st_mtime

# Преобразуем время в объект datetime
dt_object = datetime.fromtimestamp(modification_time)

# Форматируем время в нужный формат
formatted_time = dt_object.strftime("%d.%m.%Y %H:%M")

print(f"Время изменения файла: {formatted_time}")

#Этот код выполняет следующие действия:

# 1. Получает статистику файла с помощью os.stat()[1].
# 2. Извлекает время изменения файла (st_mtime) из результата[1][2].
# 3. Преобразует это время (которое представлено в секундах с начала эпохи) в объект datetime[1].
# 4. Форматирует время в нужный вам формат с помощью метода strftime()[1].
#
# Обратите внимание, что st_mtime представляет время последнего изменения содержимого файла,
# в то время как st_ctime представляет время последнего изменения метаданных файла (в Unix)
# или время создания файла (в Windows)[2][4].